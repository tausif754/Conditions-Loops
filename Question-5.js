/*
Q5. Use the while loop to print numbers from 1 to 25 in ascending and descending order.
*/

console.log("1 to 25 Ascending Order")
let ascendeingCounter=1;
while(ascendeingCounter<=25){
    console.log(ascendeingCounter)
    ascendeingCounter++;
}

console.log("25 to 1 Descending Order")
let descendingCounter=25;
while(descendingCounter>=1){
    console.log(descendingCounter)
    descendingCounter--;
}